a = int(input())
if 0 < a <= 3:
    print('Данное значение входит в диапазон от 0 до 3')
elif 3 < a <= 6:
    print('Данное значение входит в диапазон от 3 до 6')
elif 6 < a <= 10:
    print('Данное значение входит в диапазон от 6 до 10')
else:
    print('Данное значение не выполняет условия задачи')